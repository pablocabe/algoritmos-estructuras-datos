package tp2.ejercicio09;

public class ObjSumDif {
    
    private int suma;
    private int resta;

    public ObjSumDif(int suma, int resta) {
        this.suma = suma;
        this.resta = resta;
    }

    public void setSuma(int suma) {
        this.suma = suma;
    }

    public int getSuma() {
        return this.suma;
    }

    public void setResta(int resta) {
        this.resta = resta;
    }

    public int getResta() {
        return this.resta;
    }

}
